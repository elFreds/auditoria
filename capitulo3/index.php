<div id="cabecera">AUDITORÍA DE LA FUNCIÓN INFORMATICA</div>
    <div id="texto">



La función de auditoría informática ha pasado de ser una 
función meramente de ayuda al auditor financiero a ser una 
función que desarrolla un trabajo y lo seguirá haciendo en el futuro, 
más acorde con la importancia que para las organizaciones tienen los sistemas 
informáticos y de información que son su objeto de estudio y análisis. El auditor 
informático pasa a ser auditor y consultor del ente empresarial, en el que va a ser 
analista, auditor y asesor.<br><p>

No solamente de los sistemas informáticos objeto de su estudio, 
sino de las relaciones e implicaciones operativas que dichos sistemas
 tienen en el contexto empresarial.

La función de auditoría en informática en un nivel organizacional  
que le asegure la independencia y soporte requerido de la alta dirección, 
a fin de contar con una entidad confiable y eficiente. La falta de una posición
 organizacional adecuada a las características especificas que la rodean, puede 
 convertirla en foco de frustración e incertidumbre con el paso del tiempo.El control 
 y la seguridad no pueden establecerse ni supervisarse desde los niveles inferiores de 
 una empresa; su posición debe ser estratégica o por perfiles especiales del negocio,táctico. 
 Nunca se ejercerán desde un nivel operativo; la alternativa es que los haga personal profesional externo.
 <br><p>
 <p>Una vez elaborada la planeación de la auditoría, la cual servirá como plan maestro de los tiempos, costos, prioridades, y como medio
   de control de la auditoría, se debe empezar la recolección de la información.<br>
   Para ello se procederá a efectuar la revision sistematizada del área, a traves de los siguientes elementos:</p>

   <p> A) Revisión de la estructura orgánica:</p>

<ul>
   <li>Jerarquias</li>
   <li>Estructura orgánica</li>
   <li>Funciones</li>
   <li>Objetivos</li>

</ul>

<p> Se deberá revisar la situación de los recursos humanos</p>
<p>Se deberá conocer la situación en cuanto a</p>
   <ul>
      <li>Presupuesto</li>
<li>Recursos funancieros</li>
   <li>Recursos materiales</li>
<ul>

<p>Por ultimo, se deberá revisar el grado decumplimiento de los 
   documentos administrativos</P>
   <ul>
      <li>Organización</li>
      <li>Normas políticas</li>
      <li>Planes de trabajo</li>
      <li>Controles</li>
      <li>Estandares</li>
        <li>Procedimientos</li>
    </ul>

 <p>La información nos servirá para determinar:</p>
 <ul>
   <li>Si las responsabilidades en la organización están definidas adecuadamente</li>
   <li>Si la estructura organizacional está adecuada a las necesidades</li>
   <li>Si el control organizacional es el adecuado</li>
   <li>Si se tienen los objetivos y políticas adecuadas, si se encuentran vigentes</li>
    <li>Si existe la documentación de las actividades, funciones y responsabilidades</li>
    <li>Si los puestos se encuentran definidos y señaladas sus responsabilidades</li>
    <li>Si se cumplen los lineamientos organizacionales</li>
    <li>Si se tiene un programa de capacitación adecuado y si se cumple con él</li>
    <li>Si los planes de trabajo concuerdan con los objetivos de la empresa</li>
    <li>Si se cuenta con los recursos humanos necesarios que garanticen la continuidad de la operación</li>
    <li>Si se cumple con los procedimientos y controles administrativos</li>
</ul>

<p>La organizacion debe estar estructurada de tal forma que permirta lograr eficiente y eficazmente los objetivos
   , y que esto se logre a través de una adecuada toma de decisiones</p>

</p>


