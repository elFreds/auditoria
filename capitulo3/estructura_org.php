<p>Para lograr la evaluación de la estructura orgánica se deberá solicitar el manual
    de organización de la dirección, el cual deberá comprender, como mínimo:

<ul>
    <li>Organigrama con jerarquías</li>
    <li>Funciones</li>
    <li>Objetivos y políticas</li>
    <li>Análisis, descripción y evaluación de puestos</li>
    <li>Manual de procedimientos</li>
    <li>Manual de normas</li>
    <li>Instructivos de trabajo o guías de actividad</li>
    <li>Objetivos de la dirección</li>
    <li>Políticas y normas de la dirección</li>
    <li>Planeación</li>
</ul>
</p>

<p>Uno de los elementos más críticos es el relativo al personal y a su organización. Un personal
    calificado, motivado, entrenado y con la adecuada remuneración, repercute directamente en el buen desempeño
    del área de informatica.<br>

  Dentro de la estructura organizacional de la dirección de informática no existe una
evaluación concreta y aceptada de las funciones de informática. Las funciones que en una
organización son consideradas como de programadores en otra pueden ser de analista o de
analista programador, y en algunas organizaciones se han dividido ciertas funciones con
diferentes niveles; por ejemplo, programador 1, programador II, programador III.<br>
Esto ha dado por resultado, que al no existir una definición clara de los niveles, funciones
y conocimientos se haya tomado para que las personas se designen con el título que ellos
consideren pertinente; por ejemplo, ingeniero en sistemas (sin haber obtenido el grado), analista
de sistemas o bien que en algunos países existan escuelas que confieran grados académicos que
no son reconocidos oficialmente. Al analizar las organizaciones debemos tener muy en cuenta si
están definidas las funciones y la forma de evaluar a las personas que ingresan a los diferentes
niveles de la organización.<br><p>
Si no existe un organigrama en la organización, el auditor debe elaborar uno que muestre
el actual plan de organización, ya que facilita el estudio y da una imagen general de la
organización.<br>
Criterios para analizar organigramas:
<ol type=A>
<li>Agrupar funciones similares y relacionarlas entre sí.</li>
<li>Agrupar funciones que sean compatibles.</li>
<li>Localizar la actividad cerca de la función a la que sirva.</li>
<li>Localizar la actividad cerca o dentro de la función mejor preparada para realizarla.</li>
<li>No asignar la misma función a dos personas o entidades diferentes.</li>
<li>Separar las funciones de control y aquellas que serán objeto del mismo.</li>
<li>Ningún puesto debe tener dos o más líneas de dependencia jerárquica.</li>
<li>El tramo de control no debe ser exagerado, ni muy numerosos los niveles jerárquicos.</li></ol>
Cuando se estudia la estructura orgánica es importante hacer algunas anotaciones sobre
las tareas asignadas a cada puesto y responder las siguientes preguntas.<br>

¿Existen líneas de autoridad justificadas?<br>
¿Hay una extralimitación de funciones?<br>
¿Hay demasiada supervisión de funcionarios?<br>
¿Es excesiva la supervisión en general?<br>
¿Hay agrupamientos ilógicos en las unidades?<br>
¿Hay uniformidad en las asignaciones?<br>




