    <div id="texto">

Las funciones en informática puede deferir de un organismo a otro, aunque se designen con el mismo 
nombre; por ejemplo la función del programador en una organización puede ser diferente en otra 
organización.<p><br>

Se debe de asegurar al negocio un conjunto de
acciones mínimas que vuelvan rentable la auditoría en informática.
Funciones mínimas
Evaluación y verificación de los controles y procedimientos relacionados con la función
de informática dentro de la organización.
La validación de los controles y procedimientos utilizados para el aseguramiento
permanente del uso eficiente de los sistemas de información computarizados y de los recursos de
informática dentro de la organización.
<ul>
<li>a) Evaluación, verificación e implantación oportuna de los controles y
procedimientos que se requieren para el aseguramiento del buen uso y
aprovechamiento de la función de informática.</li><p>
<li>b) Aseguramiento permanente de la existencia y cumplimiento de los controles y
procedimientos que regulan las actividades y utilización de los recursos de
informática de acuerdo con las políticas de la organización.</li><p>
<li>c) Desarrollar la auditoría en informática conforme normas y políticas estandarizadas
a nivel nacional e internacional.</li><p>
<li>d) Evaluar las áreas de riesgo de la función de informática y justificar su evaluación
con la alta dirección del negocio.</li><p>
<li>e) Elaborar un plan de auditoria en informática en los plazos determinados por el
responsable de la función.</li><p>
<li>f) Obtener la aprobación formal de los proyectos del plan y difundidos entre los
involucrados para su compromiso.</li><p>
<li>g) Administrar o ejecutar de manera eficiente los proyectos contemplados en el plan
de la auditoría en informática.</li><p>
</ul>

Una vez formalizada la función en cualquiera de las situaciones organizacionales
señaladas, se define un mecanismo de administración y control de la función. Dicho mecanismo
garantizará que todos los recursos y proyectos involucrados en el proceso de desempeño y
gestión de la auditoría en informática, obedezcan los principios básicos de un proceso
administrativo.<p><br>




