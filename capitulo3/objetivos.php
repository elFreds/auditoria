    <div id="texto">

Uno de los problemas o descontentos que pueden tener el personal es el desconocimiemto 
de los objetivos de la organización, lo cual puede deberse a una falta de definición de los 
objetivos; esto provoca que no pueda tener una planeación adecuación adecuada.<p><br>
<ul>
<li>Asegurar que la función de auditoría cubra y proteja los mayores riesgos y
exposiciones existentes en el medio informático en el negocio.</li>
<li>Asegurar que los recursos de informática (hardware, software, telecomunicaciones,
servicios, personal, etc.) sean orientados al logro de los objetivos y las estrategias de
las organizaciones.</li>
<li>Asegurar la formulación, elaboración y difusión formal de las políticas, controles y
procedimientos inherentes a la auditoría en informática que garanticen el uso y
aprovechamiento óptimo y eficiente de cada uno de los recursos de informática en el
negocio.</li>
<li>Asegurar el cumplimiento formal de las políticas, controles y procedimientos
definidos en cada proyecto de auditoría en informática mediante un seguimiento
oportuno.</li></ul><p><br>
Asegurar que se den los resultados esperados por el negocio mediante la coordinación
y apoyo reciproco con:
<ol>
<li>Auditoría.</li>
<li>Asesores externos.</li>
<li>Informática.</li>
<li>Alta dirección.</li>
</ol>

Con el fin de que los objetivos y metas de la función de auditoría en informática se lleven
a cabo con éxito, es necesario considerar lo siguiente:<p>
<ul>
<li>Elaborar y formalizar los planes de auditoría en informática.</li>
<li>Organizar y administrar la función de manera eficiente.</li>
<li>Dirigir y controlar los proyectos de auditoría en informática con oportunidad.</li>
<li>Revisar y evaluar de manera periódica el desempeño de los auditores en informática.</li>
<li>Evaluar el desempeño de la función de informática.</li>
<li>Otros.</li>
</ul>



