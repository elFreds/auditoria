información que tienen lugar en el sistema.</p>

<ol>
<H3>EVALUACIÓN EN LA EJECUCIÓN</H3>
Se refire al uso de cuestioanrio para recabar datos acerca de la actuación de la aplicación 
en la computadora, con objeto de conocer que tan ben o que tan mal esta siendo usada y 
si opera eficientemente. </p>

Los cuestionarios son medios para recopilar datos acerca del uso de los recursos de la computadora 
y pueden ser cuestionariso manuales, encuestas de opciones, 
evaluación de documentación obtencio de infromacion electrónica integrada al equipo y de programas 
ejecuntandose obteniéndose en ambas las estadísticas cerca de su uso.</p>

<H3>EVALUACIÓN EN EL IMPACTO</H3>
Es la evaluación que se debe hacer sobre la manera en que afecta a la gente que interviene en 
la aplicación (usuarios) con el objetivo de determinar como la implantación y el usos del sistema
de informacion afecta a la organización distinguiendo que factores son diferentes atribuibles al sistema. 
Las principales áreas que deben interesarse son las que intervienen en la toma de decisiones y en las actividades de operación.</p>

Esta evaluación se hace con el fin de detectar a la gente involucrada, las actividades que son necesarias realizar, 
la calidad de información y el costo de operación resultante.</p>

<h3> EVALUACIÓN ECONÓMICA </H3>
La evaluación económica es una manera de medir y comparar los diversos beneficios de los recursos pesqueros y sus ecosistemas, 
y puede constituir un instrumento poderoso para ayudar a utilizarlos y ordenarlos más racionalmente.</p>

<h3>EVALUACIÓN SUBJETIVA</h3>
Partiendo de la premisa de que los usuarios son los principales afectados directamente por el sistema, 
sus puntos de vista y necesidades deberán ser considerados para la evalucion.</p>

