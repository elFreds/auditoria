<div id="texto">

<p>El  principal objetivo de la revisión preliminar es revisar el área informática para obtener información sobre cómo llevar a cabo la auditoria; se puede hacer a través de entrevistas, observación, y verificación de documentación. Al finalizarla, el auditor puede proceder de tres formas:<br>

1.    No seguir con la auditoria: Por ejemplo, si el auditor no tuviera capacidad técnica para realizar la auditoria y necesitara ayuda del propio auditado<br>

2.    Pasar a hacer una revisión detallada: para realizar la revisión minuciosa de los controles internos, esperando poder confiar en ellos, y reducir las consecuencias<br>

3.    Seguir directamente a obtener evidencias suficientes para tomar una decisión final sobre si puede ocurrir perdidas. Si no se confía en los controles internos, puede ser menos costoso realizar directamente los Test de Apoyo.<br> 
<br>
la revision preliminar recolecta informacion por medio de entrevistas con el personal de la instalacion
, la observacion de las actividades en la instalacion y la revision de la documentacion preliminar
	
