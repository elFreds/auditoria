El proceso de la prueba de consentimiento es determinar si los controles internos operan como fueron diseñados para operar. <br>El auditor debe determinar si los controles en realidad existen y si realmente trabajan confiablemente.
Además de las técnicas manuales de recolección es muy frecuente que el auditor recurra a técnicas de recolección  de información asistidas por computadora, para determinar la existencia y confiabilidad de los controles. Por ejemplo para determinar la existencia y confiabilidad de los controles de un sistema de red, se requerirá el entrar a la red y evaluar directamente al sistema. <br>
