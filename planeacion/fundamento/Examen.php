<div id="cabecera">Examen y evaluacion de la Informacion </div>
<div id="texto">
La auditoria de la informacion consiste en el examen y evaluacion periodica de los recursos informativos de una organizacion. Su objetivo es conocer la utilizacion que se hace de la informacion, las barreras que se le impone y 
el establecimiento de procesos de mejora. Uno de los pasos previos a tener presente antes de la ejecucion efectiva de una auditoria de la informacion consiste en el conocimiento de los objetivos y las prioridades de la organizacion,
 la estructura que esta posee, los estilos de gestion que se llevan a cabo y las relaciones con el entorno.<br>
 Los auditores internos deberán obtener, analizar, interpretar y documentar la información para apoyar los resultados de la auditoría. El proceso de examen y evaluación de la información es el siguiente:Se debe obtener la información de todos los asuntos relacionados con los objetivos y alcances del auditor. La información relevante apoya los hallazgos y recomendaciones de auditoría y es consistente con los objetivosde ésta. La información útil ayuda a la organización a lograr sus metas.El proceso de recabar, analizar, interpretar y documentar la información deberá supervisarse para proporcionar una seguridad razonable de que la objetividad del auditor se mantuvo y que las metas de auditoría se cumplieron.Los documentos de trabajo de la auditoría deberán ser preparados por los auditores y revisados por la gerencia de auditoría. Estos documentos deberán registrar la información obtenida y el análisis realizado, y deben apoyar las bases de los hallazgos de auditoría y las recomendaciones que se harán.
Los auditores deberán reportar los resultados del trabajo de auditoría: El auditor deberá discutir lasconclusiones y recomendaciones en los niveles apropiados de la administración antes de emitir su informe final. Los informes deberán ser objetivos, claros, concisos, constructivos y oportunos. Los informespresentarán el propósito, alcance y resultados de la auditoría y, cuando se considere apropiado, contendrán la opinión del auditor.
