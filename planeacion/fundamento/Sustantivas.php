<div id="cabecera">Pruebas Sustantivas</div>
<div id="texto">
<p>Estas pruebas tienen como finalidad  recoger datos que sirvan como testimonio para que el auditor sepa cuando pueda ocurrir algun error en algun proceso o falta de control de la informacion. Existen 8 pruebas sustantivas  diferentes:<br>
* Pruebas para identificar errores en el procesamiento o de falta de seguridad o confidencialidad<br>
* Pruebas para asegurar la calidad de los datos<br>
* Pruebas para identificar la inconsistencia de los datos<br>
* Pruebas para compara con los datos o contadores fisicos<br>
* Confirmacion de datos con fuentes externas<br>
* Pruebas para confirmar la adecuada comunicacion<br>
* Pruebas para determinar falta de seguridad<br>
* Pruebas para determinar probemas de legalidad<br><br>
Se debe analizar el costo que representa tener demasiado control, para ello podemos estimar el costo por error del sistema y su efecto y poder determinar el nivel de riesgo o seguridad requerido contra el costo de implantacion de controles y de recuperacion de informacion.<br><br>
En el mas amplio de sentido de la palabra la auditoria implica e involucra dos conceptos basicos la investigacion critica (hace referencia a la acumulacion ordenada de evidencia) y el otro concepto conclusiones ciertas (hace referencia a la interpretacion de las evidencias acumuladas)<br><br>
En la auditoria de sistemas el auditor debe participar en  tres fases; la del diseno del sistema, la de operacion y la de posterior a la auditoria. En la fase del diseno la participacion del auditor hace que disminuya su independia,  pero se considera que hay estrategias en las cuales 
se puede eliminar esta idea, como; que el auditor amplie sus conocimientos, asignar auditores en las diferentes fases de la auditoria y crear sub departamentos de auditoria.
Una auditoria en informatica es un trabajo que requiere de mucha concentracion y responsabilidad, es por eso que el auditor debe tener en claro los objetivos a logar; entonces es favorable que el auditor haga:<br><br>
* Dividir el sistema en subsistemas; aqui identificara las actividades basicas de cada uno de ellos<br><br>
* Evaluar la confianza de cada componente y de los subsistema y asi poder llegar una evaluacion general y de confianza total del sistema<br><br>
* Tener en claro el postulado de investigacion de las operaciones que dice: la suma de los optimos parciales de los subsistemas no es igual al optimo del sistema, pero nos da una buena aproximacion.<br><br>
Para hacer una buena auditoria se debe realizar en primera instancia una evaluacion del area de informatica para entender su instalacion y los principales sistemas procesados, en seguida mirar los controles internos y hacer una investigacion detallada, prosigue en dar un juicio sobre los controles criticos, y hacer pruebas sustantivas de los procedimientos para que finalmente emita un informe confiable donde concluya la situacion actual del sistema y de los pasos a seguir sobre los controles criticos<br><br> 
