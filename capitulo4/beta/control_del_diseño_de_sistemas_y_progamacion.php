    <div id="texto">



<p> El objetivo de este control es asegurarse de que el sistema funcione a las especificaciones funcionales, 
a fin de que el usuario tenga la suficiente información para su manejo. Las revisiones se efectúan en forma 
paralela, desde el análisis hasta la programación, y sus objetivos son los siguientes:

<p> --> Etapa de análisis y definición del problema: identifica con claridad cuál es el objetivo del sistema, 
eliminando inexactitudes, ambigüedades y omisiones en las especificaciones.

<p> --> Etapa de estudio de factibilidad: donde se elabora el costo/beneficio del sistema.

<p> --> Etapa de diseño: es donde se desarrolla los objetivos del sistema; donde se desarrolla e modelo lógico; 
se evalúa diferentes opciones de diseño; donde se descubren errores, todo esto antes de iniciar la codificación.

<p> --> Etapa de programación: busca la claridad, modalidad y verifica con base en las especificaciones.

<p> --> Etapa de implementación y pruebas del sistema: desarrolla la implementación del sistema con datos de 
prueba y la carga de datos definidos, evaluando el sistema, su seguridad u confidencialidad y dando entrenamiento 
a los usuarios. Esta función tiene una gran importancia en el ciclo de evaluación de aplicaciones de los sistemas 
de información, y busca comprobar que la aplicación cumpla con las especificaciones del usuario.


