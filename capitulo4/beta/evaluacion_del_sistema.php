<p>El objetivo del análisis y diseño estructurado es determinar los requerimientos exactos, de tal forma que se 
diseñe el sistema correcto. El diseño estructurado emplea una serie de herramientas gráficas y técnicas que 
permiten el análisis de tal manera que se puedan identificar errores antes de que ocurran. La programación 
estructurada recolecta información sobre procedimientos actuales, flujos de información, procesos de toma de 
decisiones y reportes.

<p>El diagrama de flujo es útil porque detecta los procesos lógicos, los requerimientos de información, provee 
un modelo grafico del sistema actual, que puede ser utilizado para encontrar mejoras. Es por esa razón que el 
diagrama de flujo se convierte en la base para desarrollar y evaluar las diferentes alternativas de diseño para 
el nuevo sistema.

<p> --> Software compartido o regalado: el software es sencillo elaborado para computadoras portátiles, los cuales 
pueden ser conseguidos a bajo precio y por vía internet. Aunque pueden tener desventajas como, en ocasiones no 
cumplen con todo lo requerido por la organización, puede ser software pirata, puede contener virus.

<P> --> Software transportable: es aquel software que tiene diferentes versiones para diferentes sistemas operativos, 
el cual permite aparentemente, usar el mismo programa de aplicación sin importar el sistema computacional. 
Una ventaja muy importante es que ahorra tiempo en entretenimiento y en persona. 

<P> --> Un usuario o multiusuario: el software puede ser realizada para ser manejado por solo un usuario o por varios 
usuarios.

<P> --> Software a la medida: este software puede ser vendido o puede ser elaborado internamente como paquetes 
individuales o integrales y compatibles que son diseñados para trabajar en conjunto.

