<p> Los sistemas distribuidos se pueden definir como el sistema en el cual las computadoras y los datos están en más 
de un lugar, así como los programas de aplicación.

<p> Las razones para implementar un sistema distribuido son:

<p> --> Mejora el tiempo de respuesta.

<p> --> Reducción de costos.

<p> --> Incremento de confianza, ya que si falla el equipo principal no significa que falle todo el sistema.

<p> Los puntos que se deben considerar al evaluar un sistema distribuido son:

<p> --> Estandarización.

<p> --> Documentación.

<p> --> Perdida de datos.

<p> --> Seguridad.

<p> --> Consistencia de datos.

<p> --> Mantenimiento del sistema.

